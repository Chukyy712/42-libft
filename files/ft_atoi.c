#include "libft.h"

int	ft_atoi(const char *str)
{
	long int	i;
	long int	signal;
	long int	res;

	res = 0;
	signal = 1;
	i = 0;
	if (!str[i])
		return (0);
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signal = -1;
		i++;
	}
	while (str[i] <= '9' && str[i] >= '0')
	{
		res = (res * 10) + (str[i] - 48);
		i++;
	}
	return (res * signal);
}
