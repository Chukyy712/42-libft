#include "libft.h"

void	*ft_memchr(const void *src, int c, size_t n)
{
	size_t			i;
	unsigned char	*str;
	unsigned char	chr;

	str = (unsigned char *) src;
	chr = (unsigned char) c;
	i = -1;
	while (++i < n)
	{
		if (str[i] == chr)
			return ((char *)&str[i]);
	}
	return (NULL);
}
