#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	len;

	len = ft_strlen((char *)s);
	while (len--)
	{
		if ((unsigned char)s[len] != (unsigned char) c)
			return (NULL);
	}
	return ((char *)&s[len]);
}
